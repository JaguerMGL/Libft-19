#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = ft_calloc(1, sizeof(*new));
	if (!new)
		return (NULL);
	new->content = content;
	new->next = (NULL);
	return (new);
}
